String mapNumberToSemester(int number) {
  switch (number) {
    case 1:
      return "1st Sem";
    case 2:
      return "2nd Sem";
    case 3:
      return "3rd Sem";
    case 4:
      return "4th Sem";
    case 5:
      return "5th Sem";
    case 6:
      return "6th Sem";
    case 7:
      return "7th Sem";
    case 8:
      return "8th Sem";
    case 9:
      return "9th Sem";
    case 10:
      return "10th Sem";
    default:
      return "1st Sem";
  }
}
